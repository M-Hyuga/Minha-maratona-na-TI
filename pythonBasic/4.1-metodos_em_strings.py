#os métodos ajudam em situações como por exemplo padronizações, definir o que um objeto pode fazer, ou seja, seu comportamento... e assim vai
mensagem =  "eu adoro comida caseira"
print(mensagem.lower())

print(mensagem.upper())

print(mensagem.capitalize())

print(mensagem.find('c'))

print(mensagem.replace('comida caseira','pizza de calabreza'))

print(mensagem.strip()) #retira os espaços na frente do conteúdo no inicio. (caso haja)

